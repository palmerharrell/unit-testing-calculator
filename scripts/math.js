"use strict";

function add(num1, num2) {
	return num1 + num2;
}

function subtract(num1, num2) {
	return num1 - num2;
}

function multiply(num1, num2) {
	return num1 * num2;
}

function divide(num1, num2) {
	return num1 / num2;
}

function square(num) {
	return Math.pow(num, 2);
}

function squareRoot(num) {
	return Math.sqrt(num);
}
